#Escape characters & Multiple Line Strings
#\n: Is to jump a line
cads = 'This is a \n Single Quotation Text'
#\t: Is to make a tabulation
cadd = "\nThis is a \n\t Double Quotation Text"
#\': Is to add the single quotation
cad = '\nThis is a \'Single Quotation Text\'...'
#A multiple line text string with triple double-quotation
cadc = """\nText Line 1
 Text Line 2
  Text Line 3
"""
#Printing...
print (cads)
print (cadd)
print (cad)
print (cadc)



