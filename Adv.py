#Single Quotation String
cads = 'Single Quotation Text'
#Double Quotation String
cadd = "Double Quotation Text"
#To check the Type of Variable:
print (type(cads))
print (type(cadd))
#To Print
print (cads)
print (cadd)

#Single Quotation String
